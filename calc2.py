def sum(*a):
    sum=0
    for i in a:
        sum=sum+i
    return sum

def diff(*a):
    sum=0
    for i in a:
        sum=sum-i
    return sum

def product(*a):
    sum=1
    for i in a:
        sum=sum*i
    return sum

def division(*a):
    sum=1
    for i in a:
        sum=sum/i
    return sum